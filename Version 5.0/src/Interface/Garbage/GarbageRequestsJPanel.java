    public void populateComboBox() {

        for (Employee e : organization.getEmpList().getEmployeeDirectory()) {
            if (e.getRole().toString().equals("GarbageStaff")) {
                EmployeeComboBox.addItem(e);
            }
        }

    }

    public void populate_table() {
        System.out.println(organization.getWorkQueue().getReqList().size());

        DefaultTableModel dtm = (DefaultTableModel) garbageReqJTable.getModel();
        //  productCatalogTbl.getColumn("Image").setCellRenderer(new ManageProductsJPanel.LabelRenderer());
        dtm.setRowCount(0);

        for (WorkRequest wr : organization.getWorkQueue().getReqList()) {

            Object Row[] = new Object[5];
            Row[0] = wr.getCust();
            Row[1] = wr;
            Row[2] = wr.getMessage();
            Row[3] = wr.getStatus();
            Row[4] = wr.getReceiver();

            dtm.addRow(Row);
        }

    }
