private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) electricityTable.getModel();
        model.setRowCount(0);
        
        for (WorkRequest wr:customer.getWorkList().getReqList())
        {  
                 if(wr instanceof ElectricityWorkRequest)
                {
                    ElectricityWorkRequest ewr = new ElectricityWorkRequest();
                    ewr = (ElectricityWorkRequest) wr;
                    Object[] row = new Object[3];
                    row[0] = wr.getCust().getElecBill();
                    row[1] = ewr.getElec_bill_days();
                    row[2] = ewr.getReqCharges();
                    model.addRow(row);   
                }

        }
    }
     
    
