    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        try{
        int days = Integer.parseInt(electricityDaysTextField.getText());
        
        //customer.setWaterBill(billno);
        customer.getElectricity().addElecDays(days);
        
        JOptionPane.showMessageDialog(null, "Days Recorded", "Information", JOptionPane.INFORMATION_MESSAGE);
        
        
        electricityDaysTextField.setEnabled(false);
        electricityDaysTextField.setText("");
        
        //Automatic generation code
        if(customer.getElectricity().getElecDays()>customer.getElectricity().getElecThreshold())
        {
        String msg = "Generate Electricity Bill";
        
        ElectricityWorkRequest er= new ElectricityWorkRequest();
        customer.getWorkList().getReqList().add(er);
        
        er.setCust(customer);
        er.setSender(account);
        er.setMessage(msg);
        Date date = new Date();
        er.setRequestDate(date);
        er.setStatus("OPEN");
        er.setElec_bill_days(days);
        
        
        electricityOrganisation1.getWorkQueue().getReqList().add(er);
        er.getCust().getNotification().addNotification(er);
        JOptionPane.showMessageDialog(null, "Automatic request for bill has been sent", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        populateTable();
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Please enter number of days in digits.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_submitButtonActionPerformed

