    public void populate_table()
    {
        DefaultTableModel model = (DefaultTableModel) orderListJTbl.getModel();
        model.setRowCount(0);
        
        for(Product p:customer.getProdList())
        {
            if(p.getAuto_refill()==true)
            {
               Object[] row = new Object[5];
                row[0] = p;
                row[1] = p.getProductQuant();
                row[2] = p.getThreshold();
                row[3] = p.getQuantity_left();
                row[4] = p.getAuto_refill();
                model.addRow(row); 
            }
            
        }
        
    }

