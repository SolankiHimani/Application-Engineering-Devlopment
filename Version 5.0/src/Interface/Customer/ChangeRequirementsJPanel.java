    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed
        // TODO add your handling code here:
        int row = orderListJTbl.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row to change values!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else
        {
            Product p2 = (Product) orderListJTbl.getValueAt(row,0);
            int thresh = ThreshholSpinner.getComponentCount();
            for(Product p1 : customer.getProdList())
            {
                if(p1==p2)
                {
                    int old_value= p1.getThreshold();
                    p1.setThreshold(thresh);
                    JOptionPane.showMessageDialog(null, "Value changed successfully from "+old_value+" to "+ thresh);
                    populate_table();
                }
                
            }
        }
        
        
    }//GEN-LAST:event_SaveBtnActionPerformed

