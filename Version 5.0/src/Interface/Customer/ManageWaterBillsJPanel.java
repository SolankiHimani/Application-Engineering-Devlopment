    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        //double billno = Double.parseDouble(waterBillNoTextField.getText());
try{
        int days = Integer.parseInt(waterBillDaysTextField.getText());
        
        //customer.setWaterBill(billno);
        customer.getWater().addWaterDays(days);
        
        JOptionPane.showMessageDialog(null, "Days Recorded", "Information", JOptionPane.INFORMATION_MESSAGE);
        
        
        waterBillNoTextField.setEnabled(false);
        waterBillDaysTextField.setEnabled(false);
        waterBillDaysTextField.setText("");
        
        //Automatic generation code
        if(customer.getWater().getWaterDays()>customer.getWater().getWaterThreshold())
        {
        String msg = "Generate Water Bill";
        
        WaterWorkRequest er= new WaterWorkRequest();
        customer.getWorkList().getReqList().add(er);
        
        er.setCust(customer);
        er.setSender(account);
        er.setMessage(msg);
        Date date = new Date();
        er.setRequestDate(date);
        er.setStatus("OPEN");
        er.setWater_bill_days(days);
        
        
        waterOrganisation1.getWorkQueue().getReqList().add(er);
         er.getCust().getNotification().addNotification(er);
        JOptionPane.showMessageDialog(null, "Automatic request for bill has been sent", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        populateTable();
}

catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Please enter number of days in digits.", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_submitButtonActionPerformed


