    private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) waterBillTable.getModel();
        model.setRowCount(0);
        
        for (WorkRequest wr:customer.getWorkList().getReqList())
        {  
//            for(WorkRequest wr3: waterOrganisation1.getWorkQueue().getReqList())
//            {
                if(wr instanceof WaterWorkRequest)
                {
                    WaterWorkRequest ewr = new WaterWorkRequest();
                    ewr = (WaterWorkRequest) wr;
                    Object[] row = new Object[3];
                    row[0] = wr.getCust().getWaterBill();
                    row[1] = ewr.getWater_bill_days();
                    row[2] = ewr.getReqCharges();
                    model.addRow(row);   
                }
//            }
        }
    }
