    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        // TODO add your handling code here:
        try{
        GroceryProduct gp = groceryOrganisation.addProduct();
        //  idField.setEnabled(false);
        gp.setProdName(nameField1.getText());
        gp.setAvail(Integer.parseInt(TextAvailibility.getText()));
        String stringPrice = priceField.getText();
        if(stringPrice.isEmpty()==false) {
            int price = Integer.parseInt(stringPrice);
            gp.setPrice(price);
        }
        gp.setImgLoc(imagePathJTxtField.getText());
        JOptionPane.showMessageDialog(null, "Product successfully added", "Warning", JOptionPane.INFORMATION_MESSAGE);

        nameField1.setText("");
        TextAvailibility.setText("");
        imagePathJTxtField.setText("");
        priceField.setText("");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Please enter valid values:", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_createButtonActionPerformed

